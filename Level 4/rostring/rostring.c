#include <unistd.h>

int main(int arg, char **tab)
{
    int i = 0;
    int k;
    
    if(arg > 1)
    {
        while(tab[1][i] == ' ' || tab[1][i] == '\t')
            i++;
        k = i;
        while(tab[1][i] && tab[1][i] != ' ' && tab[1][i] != '\t')
            i++;
        while(tab[1][i])
        {
            if(tab[1][i] && (tab[1][i] != ' ' && tab[1][i] != '\t') 
                && (tab[1][i - 1] == ' ' || tab[1][i - 1] =='\t'))
            {
                while(tab[1][i] &&(tab[1][i] != ' ' && tab[1][i] != '\t'))
                {
                    write(1, &tab[1][i], 1);
                    i++;
                }
                write(1, " ", 1);
            }
            i++;
        }
        while(tab[1][k] && (tab[1][k] != ' ' && tab[1][k] != '\t'))
        {
            write(1, &tab[1][k], 1);
            k++;   
        }
    }
    write(1, "\n", 1);
}