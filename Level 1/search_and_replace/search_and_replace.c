#include <unistd.h>

int main(int arg, char **tab)
{
	int i = 0;
	if(arg == 4)
	{
		if(((tab[2][0] >= 'a' && tab[2][0] <= 'z') || (tab[2][0] >= 'A' && tab[2][0] <= 'Z')) && tab[2][1] == '\0')
		{
			if(((tab[3][0] >= 'a' && tab[3][0] <= 'z') || (tab[3][0] >= 'A' && tab[3][0] <= 'Z')) && tab[3][1] == '\0')
			{
				while(tab[1][i])
				{
					if(tab[1][i] == tab[2][0])
						tab[1][i] = tab[3][0];
					write(1, &tab[1][i], 1);
					i++;
				}
			}
		} 
	}
	write(1, "\n", 1);
}
